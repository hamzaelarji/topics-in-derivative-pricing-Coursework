# ## 5 — Non-linear Drift Calibration (Eq. 25)
#
# The short rate AEMM replaces the linear mean reversion drift $-a_k x_k$ of G(K)++
# with a non-linear drift $\mu_k(t, x_1 \ldots x_k)$ calibrated to VAE-generated curves:
#
# $$r(t) = \varphi(t) + \sum_k x_k(t)$$
# $$dx_k = \mu_k(t, x_1 \ldots x_k) dt + \sigma_k(t) dw_k$$
#
# We calibrate μₖ by fitting to the VAE decoder's forward rate decomposition.

# %%
def calibrate_drift_time_homogeneous(
    model: VAE,
    z_data: np.ndarray,
    tenors: np.ndarray,
    sigma: np.ndarray,
    dt: float = 1/252,
) -> Dict[str, object]:
    """
    Calibrate time-homogeneous drift μₖ(x₁...xₖ) from VAE-generated curves.
    
    The drift at each point in latent space is estimated from:
    - The average forward rate change when z moves by a small amount
    - Scaled to match the model's mean reversion pattern
    
    This is a grid-based calibration that estimates drift on a 2D grid.
    
    Parameters
    ----------
    model : trained VAE
    z_data : (N, K) historical latent codes
    tenors : maturity grid
    sigma : (K,) volatility of each latent factor
    
    Returns
    -------
    dict with drift_grid, z_grid, etc.
    """
    K = z_data.shape[1]
    z_mean = z_data.mean(axis=0)
    z_std = z_data.std(axis=0)
    
    # Estimate drift from first differences of z (time-homogeneous assumption)
    dz = np.diff(z_data, axis=0)
    
    # Bin drift by current z position
    n_bins = 10
    z1_edges = np.linspace(z_mean[0] - 3*z_std[0], z_mean[0] + 3*z_std[0], n_bins + 1)
    z2_edges = np.linspace(z_mean[1] - 3*z_std[1], z_mean[1] + 3*z_std[1], n_bins + 1)
    
    drift_grid = np.full((n_bins, n_bins, K), np.nan)
    count_grid = np.zeros((n_bins, n_bins))
    
    for i in range(len(dz)):
        z_cur = z_data[i]
        i1 = np.searchsorted(z1_edges[1:], z_cur[0])
        i2 = np.searchsorted(z2_edges[1:], z_cur[1])
        if 0 <= i1 < n_bins and 0 <= i2 < n_bins:
            if np.isnan(drift_grid[i1, i2, 0]):
                drift_grid[i1, i2] = dz[i] / dt
            else:
                n = count_grid[i1, i2]
                drift_grid[i1, i2] = (drift_grid[i1, i2] * n + dz[i] / dt) / (n + 1)
            count_grid[i1, i2] += 1
    
    # Centers of bins
    z1_centers = 0.5 * (z1_edges[:-1] + z1_edges[1:])
    z2_centers = 0.5 * (z2_edges[:-1] + z2_edges[1:])
    
    return {
        "drift_grid": drift_grid,
        "count_grid": count_grid,
        "z1_centers": z1_centers,
        "z2_centers": z2_centers,
        "z1_edges": z1_edges,
        "z2_edges": z2_edges,
    }

