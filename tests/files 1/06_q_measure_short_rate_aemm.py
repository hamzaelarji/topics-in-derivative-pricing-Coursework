# ## 7 — Short Rate AEMM Simulation
#
# We simulate the short rate AEMM using:
# 1. z dynamics in latent space (with estimated drift and historical vol)
# 2. Decode z → forward rate curve at each step
# 3. Add deterministic shift φ(t)

# %%
def simulate_short_rate_aemm(
    model: VAE,
    z_init: np.ndarray,
    phi: np.ndarray,
    tenors: np.ndarray,
    vol_z: np.ndarray,
    drift_calib: dict,
    n_steps: int = 252,
    dt: float = 1/252,
    n_paths: int = 100,
    seed: int = 42,
) -> Dict[str, np.ndarray]:
    """
    Simulate Short Rate AEMM.
    
    Uses estimated mean-reverting drift in latent space + VAE decoder.
    """
    np.random.seed(seed)
    K = len(z_init)
    N = len(tenors)
    
    z_paths = np.zeros((n_paths, n_steps + 1, K))
    fwd_paths = np.zeros((n_paths, n_steps + 1, N))
    short_rates = np.zeros((n_paths, n_steps + 1))
    
    # Initial forward rates
    f0 = decode_forward_at_z(model, z_init, tenors)
    
    # Simple mean-reverting drift: μ(z) ≈ -a * (z - z_mean)
    z_mean = np.zeros(K)
    dg = drift_calib["drift_grid"]
    cg = drift_calib["count_grid"]
    # Estimate a from linear regression of drift on z
    z1c = drift_calib["z1_centers"]
    z2c = drift_calib["z2_centers"]
    
    # Rough mean reversion estimates
    a_est = np.zeros(K)
    z_target = np.zeros(K)
    for k in range(K):
        zk_vals, dk_vals = [], []
        if k == 0:
            for i in range(len(z1c)):
                mask = cg[i, :] > 3
                if mask.sum() > 0 and not np.all(np.isnan(dg[i, mask, k])):
                    dk_vals.append(np.nanmean(dg[i, mask, k]))
                    zk_vals.append(z1c[i])
        else:
            for j in range(len(z2c)):
                mask = cg[:, j] > 3
                if mask.sum() > 0 and not np.all(np.isnan(dg[mask, j, k])):
                    dk_vals.append(np.nanmean(dg[mask, j, k]))
                    zk_vals.append(z2c[j])
        
        if len(zk_vals) > 2:
            # Linear fit: drift = -a*(z - z_target)
            zk_arr = np.array(zk_vals)
            dk_arr = np.array(dk_vals)
            coeffs = np.polyfit(zk_arr, dk_arr, 1)
            a_est[k] = -coeffs[0] * dt  # convert to annualised
            z_target[k] = -coeffs[1] / coeffs[0] if abs(coeffs[0]) > 1e-10 else 0.0
    
    print(f"Estimated mean reversion: a = {a_est}")
    print(f"Estimated target: z* = {z_target}")
    
    for p in range(n_paths):
        z = z_init.copy()
        z_paths[p, 0] = z
        fwd_paths[p, 0] = f0 + phi
        short_rates[p, 0] = (f0 + phi)[0]  # approximate short rate from first forward
        
        for t in range(n_steps):
            # Drift: mean reversion
            drift = -a_est * (z - z_target)
            
            # Diffusion
            xi = np.random.randn(K)
            dz = drift * dt + vol_z * np.sqrt(dt) * xi
            z = z + dz
            
            # Decode to forward rates + shift
            f_vae = decode_forward_at_z(model, z, tenors)
            fwd_paths[p, t+1] = f_vae + phi
            z_paths[p, t+1] = z
            short_rates[p, t+1] = (f_vae + phi)[0]
    
    return {
        "z_paths": z_paths,
        "fwd_paths": fwd_paths,
        "short_rates": short_rates,
        "a_est": a_est,
        "z_target": z_target,
    }

# %%
print(f"Running Short Rate AEMM simulation for {ccy_demo}...")
sr_sim = simulate_short_rate_aemm(
    multi_vae, z0, phi, TENORS, vol_z,
    drift_calib, n_steps=252, n_paths=200, seed=42,
)

