# ## 3 — Classical G(2)++ Model (§3.2.1)
#
# The G(2)++ model (Eq. 18-19):
#
# $$r(t) = \varphi(t) + x_1(t) + x_2(t)$$
# $$dx_k = -a_k x_k dt + \sigma_k dw_k, \quad k=1,2$$
#
# The forward rate generated by this model:
# $$f(t, T) = f(0, T) + \sum_k x_k(t) e^{-a_k(T-t)} + f_{conv}(t, T)$$
#
# where $f_{conv}$ is the convexity adjustment (O(σ²)).

# %%
class G2PlusPlus:
    """
    Classical G(2)++ short rate model.
    
    Parameters: a1, a2 (mean reversion), sigma1, sigma2 (vol), rho (correlation)
    """
    
    def __init__(self, a1: float = 0.5, a2: float = 0.05,
                 sigma1: float = 0.01, sigma2: float = 0.005,
                 rho: float = -0.7):
        self.a1 = a1
        self.a2 = a2
        self.sigma1 = sigma1
        self.sigma2 = sigma2
        self.rho = rho
    
    def forward_rate_shift(self, x: np.ndarray, T: np.ndarray, t: float = 0.0) -> np.ndarray:
        """
        Shift in forward rate due to state variables x = (x1, x2):
        Δf(T) = x1 * exp(-a1*(T-t)) + x2 * exp(-a2*(T-t))
        """
        tau = T - t
        return x[0] * np.exp(-self.a1 * tau) + x[1] * np.exp(-self.a2 * tau)
    
    def convexity_adjustment(self, T: np.ndarray, t: float = 0.0) -> np.ndarray:
        """O(σ²) convexity drift in forward rates."""
        tau = T - t
        s1, s2 = self.sigma1, self.sigma2
        a1, a2 = self.a1, self.a2
        
        # Simplified convexity for G2++
        term1 = (s1**2 / (2 * a1**2)) * (1 - np.exp(-a1 * tau))**2
        term2 = (s2**2 / (2 * a2**2)) * (1 - np.exp(-a2 * tau))**2
        term3 = (self.rho * s1 * s2 / (a1 * a2)) * (1 - np.exp(-a1 * tau)) * (1 - np.exp(-a2 * tau))
        
        return term1 + term2 + term3
    
    def simulate(self, f0: np.ndarray, tenors: np.ndarray,
                 n_steps: int = 252, dt: float = 1/252,
                 n_paths: int = 100, seed: int = 42) -> np.ndarray:
        """
        Simulate G2++ paths.
        Returns swap_paths: (n_paths, n_steps+1, N_tenors)
        """
        np.random.seed(seed)
        N = len(tenors)
        K = 2
        
        # Cholesky for correlated shocks
        L = np.linalg.cholesky(np.array([
            [1.0, self.rho],
            [self.rho, 1.0]
        ]))
        
        x_paths = np.zeros((n_paths, n_steps + 1, K))
        fwd_paths = np.zeros((n_paths, n_steps + 1, N))
        fwd_paths[:, 0, :] = f0
        
        sigmas = np.array([self.sigma1, self.sigma2])
        a_vec = np.array([self.a1, self.a2])
        
        for p in range(n_paths):
            x = np.zeros(K)
            for t in range(n_steps):
                # Correlated shocks
                xi = L @ np.random.randn(K)
                
                # Euler step
                for k in range(K):
                    x[k] += -a_vec[k] * x[k] * dt + sigmas[k] * np.sqrt(dt) * xi[k]
                
                x_paths[p, t+1] = x.copy()
                
                # Forward rate shift
                delta_f = self.forward_rate_shift(x, tenors)
                conv = self.convexity_adjustment(tenors, t=0)
                fwd_paths[p, t+1] = f0 + delta_f + conv
        
        return fwd_paths, x_paths

# %% [markdown]
