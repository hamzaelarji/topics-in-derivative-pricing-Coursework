# ## 4 — Hierarchical Decomposition of VAE Forward Rates (Eq. 26)
#
# We decompose the VAE-generated forward rate into components:
#
# $$\hat{f}(\tau, z) = \sum_{k=1}^{K} \hat{f}_k(\tau, z_1 \ldots z_k)$$
#
# where each component depends only on z₁...zₖ (not higher dimensions).
# This provides the calibration target for each state variable's drift.

# %%
def decode_forward_at_z(model: VAE, z: np.ndarray, tenors: np.ndarray) -> np.ndarray:
    """Decode z → swap rates → forward rates."""
    model.eval()
    z_t = torch.from_numpy(z.astype(np.float32)).unsqueeze(0).to(device)
    with torch.no_grad():
        s_norm = model.decode(z_t).cpu().numpy()[0]
    s = denormalize_rates(s_norm)
    return swap_to_forward(s, tenors)


def hierarchical_decomposition(
    model: VAE,
    z_grid: np.ndarray,
    tenors: np.ndarray,
) -> Dict[str, np.ndarray]:
    """
    Hierarchical decomposition of VAE-generated forward rates (Eq. 26).
    
    For K=2:
    - f̂₁(τ, z₁) = f̂(τ, z₁, z₂=0) - f̂(τ, 0, 0)
    - f̂₂(τ, z₁, z₂) = f̂(τ, z₁, z₂) - f̂(τ, z₁, 0)
    
    Parameters
    ----------
    model : trained VAE
    z_grid : (n_points, K) grid of latent points
    tenors : maturity grid
    
    Returns
    -------
    dict with f_total, f_component_1, f_component_2 arrays
    """
    K = z_grid.shape[1]
    n_pts = z_grid.shape[0]
    N = len(tenors)
    
    # Reference: f at z = 0
    z_zero = np.zeros(K)
    f_zero = decode_forward_at_z(model, z_zero, tenors)
    
    f_total = np.zeros((n_pts, N))
    f_comp = {k: np.zeros((n_pts, N)) for k in range(K)}
    
    for i in range(n_pts):
        z = z_grid[i]
        
        # Full forward rate
        f_full = decode_forward_at_z(model, z, tenors)
        f_total[i] = f_full - f_zero  # shift relative to origin
        
        # Component 1: only z₁ active (z₂=...=0)
        z_partial = np.zeros(K)
        z_partial[0] = z[0]
        f_partial_1 = decode_forward_at_z(model, z_partial, tenors)
        f_comp[0][i] = f_partial_1 - f_zero
        
        # Component 2: z₂ contribution = f(z₁,z₂) - f(z₁,0)
        if K >= 2:
            f_comp[1][i] = f_total[i] - f_comp[0][i]
    
    return {
        "f_total": f_total,
        "f_zero": f_zero,
        "f_components": f_comp,
        "z_grid": z_grid,
    }

